/** Header sample-rate table order for MPEG-1 (index = header bits). */
export const MPEG1_SAMPLE_RATES: readonly number[] = [44100, 48000, 32000];

const ANALYSIS_COS = new Float64Array(32 * 64);
for (let sb = 0; sb < 32; sb++) {
    for (let i = 0; i < 64; i++) {
        ANALYSIS_COS[sb * 64 + i] = Math.cos(Math.PI / 64 * (2 * sb + 1) * (16 - i));
    }
}

/**
 * Polyphase analysis filterbank (ISO 11172-3 Annex C).
 * Reads 32 samples of one channel starting at `off` with `stride` between
 * samples, writes 32 subband values to subs[sbOff..sbOff+31]. `vbuf` is the
 * per-channel 512-sample delay line; `win64` is shared scratch of 64 doubles.
 * The 64 windowed sums are computed once instead of once per subband.
 */
export function analysisFilterbank(
    pcm: Float32Array,
    off: number,
    stride: number,
    vbuf: Float64Array,
    subs: Float64Array,
    sbOff: number,
    win64: Float64Array,
): void {
    vbuf.copyWithin(32, 0, 480);
    for (let i = 0; i < 32; i++) {
        const idx = off + i * stride;
        vbuf[31 - i] = idx < pcm.length ? pcm[idx] : 0;
    }
    for (let i = 0; i < 64; i++) {
        let y = 0;
        for (let j = 0; j < 512; j += 64) y += ANALYSIS_WIN[i + j] * vbuf[i + j];
        win64[i] = y;
    }
    for (let k = 0; k < 32; k++) {
        const base = k * 64;
        let s = 0;
        for (let i = 0; i < 64; i++) s += win64[i] * ANALYSIS_COS[base + i];
        subs[sbOff + k] = s;
    }
}

/** MSB-first bit writer over a zero-initialized frame buffer (OR-only). */

/** MSB-first bit reader; throws RangeError past the end of the buffer. */
export class BitReader {
    private bitPos: number;

    constructor(private readonly data: Uint8Array, startBit = 0) {
        this.bitPos = startBit;
    }

    read(count: number): number {
        let value = 0;
        for (let i = 0; i < count; i++) {
            const byte = this.bitPos >> 3;
            if (byte >= this.data.length) throw new RangeError('BitReader: read past end of data');
            value = (value << 1) | ((this.data[byte] >> (7 - (this.bitPos & 7))) & 1);
            this.bitPos++;
        }
        return value;
    }

    get position(): number {
        return this.bitPos;
    }
}

export class BitWriter {
    readonly buf: Uint8Array;
    pos: number;

    constructor(buf: Uint8Array, byteStart: number) {
        this.buf = buf;
        this.pos = byteStart * 8;
    }

    put(value: number, bitCount: number): void {
        let pos = this.pos;
        const buf = this.buf;
        for (let bit = bitCount - 1; bit >= 0; bit--) {
            if ((value >>> bit) & 1) buf[pos >>> 3] |= 128 >>> (pos & 7);
            pos++;
        }
        this.pos = pos;
    }
}

/** CBR frame sizing with the standard padding accumulator (144 * bitrate / rate). */
export class FrameSizer {
    private readonly base: number;
    private readonly remainder: number;
    private readonly sampleRate: number;
    private acc = 0;

    constructor(bitrateKbps: number, sampleRate: number) {
        const num = 144000 * bitrateKbps;
        this.base = Math.floor(num / sampleRate);
        this.remainder = num % sampleRate;
        this.sampleRate = sampleRate;
    }

    /** Returns { size, padding } for the next frame. */
    next(): { size: number; padding: 0 | 1 } {
        this.acc += this.remainder;
        if (this.acc >= this.sampleRate) {
            this.acc -= this.sampleRate;
            return { size: this.base + 1, padding: 1 };
        }
        return { size: this.base, padding: 0 };
    }

    get maxFrameSize(): number { return this.base + 1; }
}
