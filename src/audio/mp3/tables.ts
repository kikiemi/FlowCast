export class Bits {
    readonly buf: Uint8Array;
    pos: number;
    constructor(buf: Uint8Array, byteStart: number) {
        this.buf = buf;
        this.pos = byteStart * 8;
    }
    put(value: number, bitCount: number): void {
        for (let bitIndex = bitCount - 1; bitIndex >= 0; bitIndex--) {
            const byteIndex = this.pos >>> 3;
            const shift = 7 - (this.pos & 7);
            if ((value >>> bitIndex) & 1) this.buf[byteIndex] |= (1 << shift);
            else this.buf[byteIndex] &= ~(1 << shift);
            this.pos++;
        }
    }
}


export function mdct18(cur: Float64Array, prev: Float64Array, out: Float64Array): void {
    const x = new Float64Array(36);
    for (let i = 0; i < 18; i++) {
        x[i] = prev[i] * MDCT_WIN[i];
        x[i + 18] = cur[i] * MDCT_WIN[i + 18];
    }
    for (let k = 0; k < 18; k++) {
        let s = 0;
        for (let n = 0; n < 36; n++) s += x[n] * MDCT_COS[n * 18 + k];
        out[k] = s / 9.0;
    }
    for (let i = 0; i < 18; i++) prev[i] = cur[i];
}

export function applyFrequencyInversion(subs: Float64Array, off: number): void {
    for (let sb = 1; sb < 32; sb += 2) {
        for (let ss = 1; ss < 18; ss += 2) subs[off + ss * 32 + sb] = -subs[off + ss * 32 + sb];
    }
}

export function applyAntialias(spec: Float64Array, off: number): void {
    for (let sb = 1; sb < 32; sb++) {
        const lo = off + (sb - 1) * 18;
        const hi = off + sb * 18;
        for (let i = 0; i < 8; i++) {
            const lower = spec[lo + 17 - i];
            const higher = spec[hi + i];
            spec[lo + 17 - i] = lower * ALIAS_CS[i] + higher * ALIAS_CA[i];
            spec[hi + i] = higher * ALIAS_CS[i] - lower * ALIAS_CA[i];
        }
    }
}

export function tableMaxValue(table: HTable): number {
    return table.maxval + (table.linbits > 0 ? ((1 << table.linbits) - 1) : 0);
}

export function candidateTables(maxval: number): HTable[] {
    if (maxval <= 0) return [HT0];
    return PAIR_TABLES.filter((table) => table.id !== 4 && table.id !== 14 && tableMaxValue(table) >= maxval);
}

